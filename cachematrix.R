## We are task to to write an R function that can cache computations that may take a long time
## x will serves as matrix
## and s will serves as NULL

## Write a short comment describing this function

makeCacheMatrix <- function(x = matrix(sample(1:200,9),4,4)) {
  s <- NULL
  set <- function(y) {
    x <<- y
    s <<- NULL
  }
  get <- function() x
  setsolve <- function(solve) s <<- solve
  getsolve <- function() s
  list(set = set, get = get,
       setsolve = setsolve,
       getsolve = getsolve)
}

## Turn "mean" imto solve"
cacheSolve <- function(x, ...) {
  s <- x$getsolve()
  if(!is.null(s)) {
    message("getting inversed matrix")
    return(s)
  }
  data <- x$get()
  s <- solve(data, ...)
  x$setsolve(s)
  s
}
